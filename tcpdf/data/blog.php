 * Fonction pour récupérer les catégories uniques
 * 
 * @return array Liste des catégories
 */
function getCategories() {
    global $blogArticles;
    $categories = [];
    
    foreach ($blogArticles as $article) {
        if (!in_array($article['category'], $categories)) {
            $categories[] = $article['category'];
        }
    }
    
    return $categories;
}
